# 10 dahil değil, 0 dan 10 a kadar yazdırır
for i in range(10):
    print(i)

# 0'dan 10'a kadar olan sayıları yazdırır.

for i in range(10, 20):
    print(i)

# 10'dan 20'ye kadar olan sayıları yazdırır.

for i in range(10, 20, 2):
    print(i)

